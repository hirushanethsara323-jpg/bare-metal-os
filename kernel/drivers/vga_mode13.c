void vga13_clear(uint8_t color) {
    uint8_t* fb = VGA13_MEMORY;
    for (int i = 0; i < VGA13_WIDTH * VGA13_HEIGHT; i++) {
        fb[i] = color;
    }
}

void vga13_draw_pixel(int x, int y, uint8_t color) {
    if (x >= 0 && x < VGA13_WIDTH && y >= 0 && y < VGA13_HEIGHT) {
        VGA13_MEMORY[y * VGA13_WIDTH + x] = color;
    }
}

void vga13_draw_rect(int x, int y, int width, int height, uint8_t color) {
    for (int r = 0; r < height; r++) {
        for (int c = 0; c < width; c++) {
            vga13_draw_pixel(x + c, y + r, color);
        }
    }
}

void vga13_draw_line(int x0, int y0, int x1, int y1, uint8_t color) {
    int dx = (x1 > x0) ? (x1 - x0) : (x0 - x1);
    int sx = (x0 < x1) ? 1 : -1;
    int dy = (y1 > y0) ? (y0 - y1) : (y1 - y0);
    int sy = (y0 < y1) ? 1 : -1;
    int err = dx + dy, e2;

    while (1) {
        vga13_draw_pixel(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void vga13_draw_char(int x, int y, char c, uint8_t color) {
    uint8_t glyph_idx = (uint8_t)c;
    if (glyph_idx >= 128) return;

    for (int row = 0; row < 8; row++) {
        uint8_t b = font_8x8_basic[glyph_idx][row];
        for (int col = 0; col < 8; col++) {
            if (b & (0x80 >> col)) {
                vga13_draw_pixel(x + col, y + row, color);
            }
        }
    }
}

void vga13_draw_string(int x, int y, const char* str, uint8_t color) {
    int cur_x = x;
    while (*str) {
        vga13_draw_char(cur_x, y, *str, color);
        cur_x += 8;
        str++;
    }
}

/**
 * Draw Graphical Desktop Window Box with Header Bar and Close Button.
 */
void vga13_draw_gui_window(int x, int y, int w, int h, const char* title) {
    /* Window Shadow */
    vga13_draw_rect(x + 4, y + 4, w, h, COLOR13_DARK_GREY);

    /* Main Window Frame Body */
    vga13_draw_rect(x, y, w, h, COLOR13_LIGHT_GREY);

    /* Title Bar Header */
    vga13_draw_rect(x, y, w, 14, COLOR13_BLUE);

    /* Title String */
    if (title != NULL) {
        vga13_draw_string(x + 6, y + 3, title, COLOR13_WHITE);
    }

    /* Close Button [X] */
    vga13_draw_rect(x + w - 12, y + 2, 10, 10, COLOR13_RED);
    vga13_draw_char(x + w - 11, y + 3, 'X', COLOR13_WHITE);

    /* Inner Border Contour */
    for (int i = 0; i < w; i++) {
        vga13_draw_pixel(x + i, y, COLOR13_WHITE);
        vga13_draw_pixel(x + i, y + h - 1, COLOR13_BLACK);
    }
    for (int i = 0; i < h; i++) {
        vga13_draw_pixel(x, y + i, COLOR13_WHITE);
        vga13_draw_pixel(x + w - 1, y + i, COLOR13_BLACK);
    }
}

/**
 * Draw Mouse Cursor Overlay on 256-color Framebuffer.
 */
void vga13_draw_cursor(int mouse_x, int mouse_y) {
    int cx = (mouse_x * VGA13_WIDTH) / 80;
    int cy = (mouse_y * VGA13_HEIGHT) / 25;

    /* Arrow Pointer Pixels */
    vga13_draw_pixel(cx, cy, COLOR13_WHITE);
    vga13_draw_pixel(cx + 1, cy + 1, COLOR13_WHITE);
    vga13_draw_pixel(cx + 2, cy + 2, COLOR13_WHITE);
    vga13_draw_pixel(cx + 3, cy + 3, COLOR13_WHITE);
    vga13_draw_pixel(cx + 4, cy + 4, COLOR13_WHITE);
    vga13_draw_pixel(cx, cy + 1, COLOR13_WHITE);
    vga13_draw_pixel(cx, cy + 2, COLOR13_WHITE);
    vga13_draw_pixel(cx, cy + 3, COLOR13_WHITE);
    vga13_draw_pixel(cx, cy + 4, COLOR13_WHITE);
    vga13_draw_pixel(cx + 1, cy, COLOR13_WHITE);
    vga13_draw_pixel(cx + 2, cy, COLOR13_WHITE);
}
